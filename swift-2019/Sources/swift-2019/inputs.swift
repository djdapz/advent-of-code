extension String {
    func parseLineSeparatedInput() -> [Int] {
        self.split(separator: "\n").map {
            $0.trimmingCharacters(in: .whitespacesAndNewlines)
        }.map {
            Int($0) ?? 0
        }
    }

    func parseCommaSeparatedInput() -> [Int] {
        self.split(separator: ",").map {
            $0.trimmingCharacters(in: .whitespacesAndNewlines)
        }.map {
            Int($0) ?? 0
        }
    }
}
